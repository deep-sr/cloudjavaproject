package flowcontrolstatements;

public class DaysofWeek {

	public static void main(String[] args) {
	
		int day=7;
		switch(day)
		{
		case 1:
			System.out.println("Horrifying Monday...");
			break;
		case 2:
			System.out.println("Its just a Tuesday");
			break;
		case 3:
			System.out.println("Ahh its Wednesday");
			break;
		case 4:
			System.out.println("Happy Thursday");
			break;
		case 5:
			System.out.println("Fundo Friday");
			break;
		case 6:
			System.out.println("Awsome Saturday");
			break;
		case 7:
			System.out.println("Sunday Funday and next day is oh..Monday");
			break;
			default:
				System.out.println("none of the cases are matching");
			break;
		}

	}

}
