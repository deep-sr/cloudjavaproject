package flowcontrolstatements;

public class SwitchDemo {

	public static void main(String[] args) {
		int x=4;
		switch(x)
		{
		case 1:
			System.out.println("case 1 got executed");
			break;
		case 2:
			System.out.println("case 2 got executed");
			break;
		case 3:
			System.out.println("case 3 got executed");
			break;
		case 4:
			System.out.println("case 4 got executed");
			break;
			
		}

	}

}
