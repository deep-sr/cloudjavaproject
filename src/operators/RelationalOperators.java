package operators;

public class RelationalOperators {

	public static void main(String[] args) {
	int a=10;
	int b=20;
	int c=10;
	System.out.println(a<b); //10<20=true
	System.out.println(a<=c);//10<=20=true
System.out.println(a>b);
System.out.println(a>=b);
System.out.println(a==c);
System.out.println(a!=c);
	}

}
